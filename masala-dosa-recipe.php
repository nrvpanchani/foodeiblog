										<li>¼ tsp Asafoetida</li>
										<li>6-7 sambhar onions</li>
										<li>2 Large Potatoes - boiled</li>
										<li>6-7 curry leaves</li>
										<li>Salt - to taste</li>
										<li>Butter for roasting</li>
									</ul>
