							<div class="single-post__recipe__details__direction">
								<h5>Directions</h5>
								<h6>I. For Dosa Batter</h6>
								<ul>
									<li><span>1.</span> Add rice and fenugreek leaves in a separate bowl along with sufficient water to soak and soak for 5 hours. Soak the chana dal and urad dal separately.</li>
									<li><span>2.</span> Drain the excess water from the soaked rice, fenugreek seeds and chana dal, transfer to a mixer jar and blend into a smooth paste.
									</li>
									<li><span>3.</span> Transfer the prepared paste to an empty and clean bowl, add sugar and mix well. Set aside till in use.","Cover and set aside in a warm place to ferment for 5 hours.</li>
									<li><span>4.</span> Once fermented, you will notice that the batter has risen. This is an indication of a well fermented batter.
									</li>
									<li><span>5.</span> Add salt once and give this batter a slight mix. Keep aside till in use.</li>
								</ul>
								<h6 style="margin-top: 10px;">II. For Bhaji</h6>
								<ul>
									<li><span>1.</span> Heat oil in a pan and splutter mustard seeds, green chillies and asafoetida.</li>
									<li><span>2.</span> Add onions and saute till they are translucent.
									</li>
									<li><span>3.</span> Add boiled potatoes, curry leaves and salt.</li>
									<li><span>4.</span> Lastly add coriander leaves and turn off the flame.
									</li>
								</ul>
								<h6 style="margin-top: 10px;">III. For Making Dosa</h6>
								<ul>
									<li><span>1.</span> Heat a non-stick tawa and pour a ladle full batter on it. Spread it into a circular motion with a help of the ladle.</li>
									<li><span>2.</span> Add butter on the edges and a tsp in the centre. Cook on low flame.
									</li>
									<li><span>3.</span> Spread a portion prepared potato mixture all over the dosa.</li>
									<li><span>4.</span> Cook till crisp and golden brown.
									</li>
									<li><span>5.</span> Fold over and serve hot with coconut chutney and sambhar.
									</li>
								</ul>
