									<h5>Ingredients</h5>
									<h6>I. For Dosa Batter</h6>
									<ul>
										<li>1 ½ cups raw rice</li>
										<li>¾ cup urad dal - soaked</li>
										<li>1 tsp Fenugreek Seeds - soaked</li>
										<li>2 tsp Chana dal - soaked</li>
										<li>1 tsp Fenugreek Seeds - soaked</li>
										<li>Salt to taste</li>
										<li>1 tsp sugar</li>
									</ul>
									<h6 style="margin-top: 10px;">II. For Bhaji</h6>
									<ul>
										<li>1 tbsp Oil</li>
