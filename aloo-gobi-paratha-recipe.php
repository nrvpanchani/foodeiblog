									<h5>Ingredients</h5>
									<h6>I. For Aloo Paratha</h6>
									<ul>
										<li>Wheat Flour - 2 cups + extra for dusting</li>
										<li>Oil/Ghee - to toast</li>
										<li>Salt - to taste</li>
										<li>Potato(Aloo) - 2 medium sized(comes to 1 cup after mashing)</li>
										<li>Cauliflower(Gobi) florets - 1/2 cup</li>
										<li>Ajwain / Jeera - 3/4 cup</li>
										<li>Ginger green chilli paste - 1/2 tsp</li>
										<li>Turmeric powder - 1/4 tsp</li>
