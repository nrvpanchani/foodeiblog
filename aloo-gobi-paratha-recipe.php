							<div class="single-post__recipe__details__direction">
								<h5>Directions</h5>
								<ul>
									<li><span>1.</span> In a wide mixing bowl add wheat flour, salt and first five a quick mix for salt to be even. Then add water( you can even use the drained radish water) little by little, gather to form a soft pliable dough. Once it is gathered to a mass add 1/2 tsp oil and knead it once. The dough should not be too tight or too loose, it should be non sticky at this stage. Allow it to rest for a while until we prepare the stuffing.</li>
									<li><span>2.</span> First add the gobi florets in salted warm water to get rid of the worms thencook it until soft. While the gobi cooks, pressure cook aloo for 4 whistles, peel off the skin. Drain water completely, once both the veggies are cooked, mash it up with a masher.</li>
									<li><span>3.</span> Mash it well so that no lumps are there...Heat oil - add ajwain, gingerchilli past, then add mashed aloo and gobi along with turmeric, red chilli, coriander powders and required salt.","Mix well and cook for 3mins in low flame. Add chat masala powder, give a quick stir and switch off. Add coriander leaves and mix well. Flour the surface and roll our 2 parathas of same size as shown below.</li>
									<li><span>4.</span> On one paratha place 2 spoon fulls of aloo gobi filling. Now close it with another paratha...Press the edges and seal it with your fingers to avoid the filling oozing out.","Now slowly roll them into thin/thick parathas of your choice...dust flour then add there to avoid sticking. Heat a dosa pan and carefully transfer the paratha, flip to other side, drizzle oil and cook till both the side has golden spots. Repeat the same with remaining dough.Brush with ghee while serving.</li>
								</ul>
