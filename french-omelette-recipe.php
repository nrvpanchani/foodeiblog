								<h5>Directions</h5>
								<ul>
									<li><span>1.</span> BEAT eggs, water, salt and pepper in small bowl until blended.</li>
									<li><span>2.</span> HEAT butter in 6 to 8-inch nonstick omelet pan or skillet over medium-high heat until hot. TILT pan to coat bottom. POUR egg mixture into pan. Mixture should set immediately at edges.</li>
									<li><span>3.</span> Gently PUSH cooked portions from edges toward the center with inverted turner so that uncooked eggs can reach the hot pan surface. CONTINUE cooking, tilting pan and gently moving cooked portions as needed.</li>
									<li><span>4.</span> When top surface of eggs is thickened and no visible liquid egg remains, PLACE filling on one side of the omelet. FOLD omelet in half with turner. With a quick flip of the wrist, TURN pan and INVERT or SLIDE omelet onto plate. SERVE immediately.</li>
