								<h5>Directions</h5>
								<h6>I. Manchurian Balls</h6>
								<ul>
									<li><span>1.</span> In a mixing balls - add all the ingredients - mix well.</li>
									<li><span>2.</span> In a kadhai - heat oil for deep frying.</li>
									<li><span>3.</span> Fry manchurian balls till golden brown and crispy.</li>
									<li><span>4.</span> Remove in a kitchen towel. Keep aside for further use.</li>
								</ul>
								<h6 style="margin-top: 10px;">II. Manchurian Dry</h6>
								<ul>
									<li><span>1.</span> In a wok/ kadhai - add oil.</li>
									<li><span>2.</span> Add ginger garlic and green chillies - mix well and cook for 2 minutes.
									</li>
									<li><span>3.</span> Add soy sauce - mix well.</li>
									<li><span>4.</span> Add spring onion greens and coriander leaves - mix well and cook for 2 minutes.</li>
									<li><span>5.</span> Add cornstarch slurry and mix well.</li>
									<li><span>6.</span> Add Manchurian balls - mix well and cook for another 2 minutes.</li>
									<li> Serve hot.</li>
