									<h5>Ingredients</h5>
									<h6>I. For Manchurian Balls</h6>
									<ul>
										<li>¼ cup Spring Onion Greens- finely chopped</li>
										<li>¼ cup Cabbage- finely chopped</li>
										<li>¼ cup Green Beans- finely chopped</li>
										<li>¼ cup Carrots- finely chopped</li>
										<li>1 inch-Ginger- finely chopped</li>
										<li>Salt to taste</li>
										<li>1 tsp White Pepper powder</li>
										<li>1 cup Refined Flour</li>
										<li>2 tbsp Cornstarch</li>
										<li>Oil for deep frying</li>
									</ul>
									<h6 style="margin-top: 10px;">II. For Manchurian Dry</h6>
									<ul>
										<li>2 tbsp Oil</li>
